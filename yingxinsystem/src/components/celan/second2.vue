二、学费、住宿费、代管费交费程序
		
1.在线缴费：新生在8月20日后，访问“四川师范大学迎新专题网”，登录“网上报到”系统，在“财务缴费”页面，点击“在线缴费”，按照“网上缴费操作流程”说明进行足额缴费。
		
