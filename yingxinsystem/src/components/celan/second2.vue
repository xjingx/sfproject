1.《中华人民共和国高等教育法》（1998年8月）第五十四条规定：高等学校的学生应当按照国家规定缴纳学费。家庭经济困难不能按时缴纳学费的学生，到校后应及时办理缓交手续，<br>并可根据相关政策申请补助或者减免学费，或按照国家现行有关学生资助政策体系的规定，申请助学贷款。

