新生报到期间，仅有如下公示三家单位可向学生收取相关费用：
		
1.计划财务处代表学校收取学费、住宿费、代管费（详见收费标准）；
		
2.中国人民财产保险股份有限公司成都市分公司收取学生平安保险费：200元/年（4年）；150元/年（3年）；100元/年（2年）（本着自愿原则办理）；
		
