5.学校狮子山校区设有工商银行储蓄所、建设银行ATM、农业银行ATM、中国银行ATM、上海浦东发展银行ATM；东校区设有工商银行ATM、农业银行ATM、建设银行ATM、农商银行ATM；<br>成龙校区设有中国银行ATM、农业银行ATM、工商银行ATM、建设银行ATM，需要在相关银行办理相关业务的新生可以前去办理。

